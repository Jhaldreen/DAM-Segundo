    private void ponLaAyuda() 
    {
        try 
        {
            // Carga el fichero de ayuda
            
            ClassLoader loader = getClass().getClassLoader();       
            URL helpSetURL = HelpSet.findHelpSet(loader, "help_set.hs");
            // Crea el HelpSet y el HelpBroker
            HelpSet helpset = new HelpSet(getClass().getClassLoader(), helpSetURL);
            HelpBroker hb = helpset.createHelpBroker();
            hb.enableHelpOnButton(jMenuItem1, "ventana_datos", helpset); //Item de menú            
            hb.enableHelpOnButton(botonAyuda, "aplicacion", helpset); //Item de menú            
            hb.enableHelpKey(getRootPane(),"aplicacion",helpset); //F1  
            hb.setLocation(new Point(300,300));
         
        } 
        catch (Exception e) 
        {
            e.printStackTrace();
        }
    }
