    private void botonInformeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInformeActionPerformed
        java.util.Calendar cal=java.util.Calendar.getInstance();
        String aleat=String.valueOf(cal.getTimeInMillis());
        try{            
            String para="";
            if (seleccion.getSelectedIndex()!=0){
                para=(String) seleccion.getSelectedItem();
            }
            Map parametros=new HashMap();
            parametros.put("n_PARAM",para);     
            JasperReport jasperReport = (JasperReport) JRLoader.loadObject(getClass().getResource("Reporte_Ejemplo.jasper"));
            JasperPrint print = JasperFillManager.fillReport(jasperReport, parametros,con);
            
            
            
            JasperViewer jv=new JasperViewer(print,false);
            jv.setVisible(true);
            jv.show();
            JasperExportManager.exportReportToPdfFile(print,"listado_poder"+seleccion.getSelectedItem().toString()+aleat+".pdf");
        }catch(JRException e){
            System.out.println("Error al generar el informe "+e);
        }
        try {
            File path = new File ("listado_poder"+seleccion.getSelectedItem().toString()+aleat+".pdf");
            Desktop.getDesktop().open(path);
        }catch (IOException ex) {
            System.out.println("Error abriendo informe "+ex);
        }
        
    }//GEN-LAST:event_botonInformeActionPerformed

